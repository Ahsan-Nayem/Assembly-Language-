.MODEL SMALL
.DATA
ASK DB 0AH,0DH, "Enter your name:$"
MSG DB ?
MSG1 DB " $" 


.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

LEA DX,ASK
MOV AH,09H
INT 21H

MOV si ,OFFSET MSG

INPUT:
MOV AH,1
INT 21H
CMP AL,13
JE DISPLAY
MOV [si],AL
INC si
JMP INPUT

DISPLAY:


MOV [si],'$'
MOV di,OFFSET MSG
MOV DL,13
MOV AH,2
INT 21H
MOV DL,10
MOV AH,2
INT 21H

AGAIN:
CMP [di],'$'
JE EXIT
CMP [di],32
JE NEXT
MOV DL,[di]
MOV AH,2
INT 21H
INC di
JMP AGAIN

NEXT:
MOV AH,2
MOV DL,32
INT 21H

INC di
JMP AGAIN

EXIT:
MOV AH,4CH
INT 21H

MAIN ENDP 
END MAIN










